// 022. 비교 연산자

// 일치 연산자
console.log(5 == 5); // true
console.log("5" == 5); // true
console.log(5 == '5'); // true
console.log(5 != 1); // true
console.log(5 != "1"); // true
console.log(5 != "5"); // false
console.log(5 === 5); // true
console.log(5 === "5"); // false
console.log(5 !== 10); // true
console.log(5 !== "5"); // false

// 관계 연산자
console.log(5 > 3); // true
console.log(5 < 3); // false
console.log(5 <= 6); // true
console.log(5 >= 5); // true

console.log("---------");
console.log("Italy" > "America");
console.log("Korea" < "korea");
